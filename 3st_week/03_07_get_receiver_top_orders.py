def get_receiver_top_orders(heights):
    result = [0] * (len(heights))
    for i in range(len(heights)-1):
        cur_num = heights.pop()
        cur_len = len(heights)
        for j in range(cur_len-1, -1, -1):
            if heights[j] >= cur_num:
                result[cur_len] = j+1
                break
        else:
            result[cur_len] = 0
    return result

